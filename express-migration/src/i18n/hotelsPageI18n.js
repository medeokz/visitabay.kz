function pickLang(locale) {
  const n = normalizeLang(locale) || PRIMARY_LOCALE;
  if (n === "ru" || n === "en" || n === "kk") return n;
  return PRIMARY_LOCALE;
}

function getHotelsCatalog(locale) {
  const l = pickLang(locale);
  return HOTELS_CATALOG[l] || HOTELS_CATALOG[PRIMARY_LOCALE];
}

function getHotelsDetail(locale) {
  const l = pickLang(locale);
  return HOTELS_DETAIL[l] || HOTELS_DETAIL[PRIMARY_LOCALE];
}

function attachHotelsI18n(pack, locale) {
  const l = pickLang(locale);
  return {
    ...pack,
    hotelsCatalog: HOTELS_CATALOG[l] || HOTELS_CATALOG[PRIMARY_LOCALE],
    hotelsDetail: HOTELS_DETAIL[l] || HOTELS_DETAIL[PRIMARY_LOCALE]
  };
}

